package fr.d3us.bo;

public class Type {

	private int id;
	private String nom;
	
	public Type() {
		super();
	}
	public Type(int id, String nom) {
		super();
		this.id = id;
		this.nom = nom;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	
}
